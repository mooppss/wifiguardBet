static const int ouiTableSize = sizeof(ouiTable) / sizeof(ouiTable[0]);

void ouiLookup(NetworkRecord& net) {
  net.vendor[0] = '\0';
  for (int i = 0; i < ouiTableSize; i++) {
    if (net.bssid[0] == ouiTable[i].oui[0] &&
        net.bssid[1] == ouiTable[i].oui[1] &&
        net.bssid[2] == ouiTable[i].oui[2]) {
      strncpy(net.vendor, ouiTable[i].name, 15);
      net.vendor[15] = '\0';
      return;
    }
  }
}
